export const LangContext = createContext('pt');

export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('pescamon-lang') || 'pt');

  function changeLang(code) {
    setLang(code);
    localStorage.setItem('pescamon-lang', code);
  }

  return (
    <LangContext.Provider value={{ lang, changeLang }}>
      {children}
    </LangContext.Provider>
  );
}

export function useT() {
  const { lang } = useContext(LangContext);
  return (key) => translations[lang]?.[key] ?? translations['pt'][key] ?? key;
}

export function useLang() {
  return useContext(LangContext);
}
